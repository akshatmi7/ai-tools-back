package com.aiintelli.aitoolsummarygenerator;

import jakarta.persistence.*;

@Entity
public class Pros {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne
    @JoinColumn(name = "tool_id")
    private AITool tool;

    private String text;

    // Getters and Setters

    public Pros() {
    }

    public Pros(Long id, AITool tool, String text) {
        this.id = id;
        this.tool = tool;
        this.text = text;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public AITool getTool() {
        return tool;
    }

    public void setTool(AITool tool) {
        this.tool = tool;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
